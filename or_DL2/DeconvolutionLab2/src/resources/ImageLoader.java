package resources;

import java.awt.Image;
import java.net.URL;

import javax.swing.ImageIcon;

public class ImageLoader {
	
	public static Image get(String filename) {
		URL url = ImageLoader.class.getResource(filename);
		if (url != null) {
			ImageIcon img = new ImageIcon(url, "") ;  
			return img.getImage();
		}
		return null;
	}

}
